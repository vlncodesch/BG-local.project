    <p>&nbsp; &nbsp; &nbsp; &nbsp; 2.7. Exceptions to the Rules:</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 2.7.1. If Player or Banker is dealt 8 or 9 points in the first two cards, such hand is called a Natural hand, and no more cards are dealt. The total sums of the Player and the Banker points are calculated and the winner is announced. This rule supersedes all other rules.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 2.7.2. If Player is dealt 6 or 7 points in the first two cards, and Banker is dealt 5 points, or less (0, 1, 2, 3, 4) in the first two cards as well, then the Banker gets the third card.</p>
    <p><strong>3. Gameplay:</strong></p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 3.1. The first betting round. Bets can be placed even before the dealing of the first card. Punters can bet on one or more available outcomes.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 3.2. The second betting round. After the first betting round the Player receives the first card and new odds for the betting outcomes are displayed on screen and the second betting round begins. Bets made in the first betting round have no influence for bets of the second betting round therefore punters can place bets on the same or different outcomes more than once.&nbsp;</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 3.3. The third betting round. After the second betting round the Banker receives the first card and new odds for the betting outcomes are displayed on screen and the third betting round begins. After this betting round the Player and the Banker receives the second and, if necessary, the third card each (see 2.6). The results are calculated and the winner of the draw is displayed on screen.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 3.4. Change of cards:</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 3.4.1. When less than 40 cards remain in the shoe, the dealer announces that the shoe will be changed after the ongoing draw.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 3.4.2. The dealer places all used and unused cards to the used card box. Empty shoe is replaced with a full and ready to use shoe with shuffled cards. The full used card box is replaced with an empty box as well. This change is made real-time when punters and audience can see it.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 3.5. The shuffling of cards. ; comes and shuffles the cards while everyone sees it and prepares the cards for dealing. Both shoes are visible on screen all the time so punters can see the shuffling and change of cards.</p>
    <p><strong>4.&nbsp;</strong><strong>Betting limits:</strong></p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 4.1. Maximum and minimum betting limits are determined by the gambling company.</p>
    <p><strong>5. Special cases:</strong></p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 5.1. Cancelled draws. Bet-on-Baccarat game draws may be cancelled if:</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 5.1.1.&nbsp; The scanner cannot scan the card or its readings do not match the card on the table;</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 5.1.2. A card or cards appear in a wrong place or gets mixed;</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 5.1.3. Technical problems occur (internet connection problems, technical failures in the studio or dealer&rsquo;s mistakes).</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;5.1.3.1. Dealer&rsquo;s mistakes that can cause the draw to be cancelled:</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;5.1.3.1.1. The dealing sequence is altered(see 2.2);</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;5.1.3.1.2. A card or cards are marked or damaged;</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;5.1.3.1.3. A card or cards fall off the table or cannot be seen on screen due to the dealer&rsquo;s mistake;</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;5.1.3.1.4. A card or cards in the deck are turned face-up and picture/number is visible due to incorrect shuffling;</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;5.1.3.1.5. The dealer incorrectly scans a card or cards and readings of the scanner do not match the cards on the table.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 5.1.4. If the draw is cancelled, all bets are void and stakes returned to players (odds are equalized to one).</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 5.2. Card burning. Cards can be burned during the game if:</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 5.2.1. The dealer takes and shows a card face-up from the shoe when the betting round is not finished;</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 5.2.2. A card cannot be scanned or read by the program due to technical reasons.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 5.3. Card burning procedure:</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 5.3.1. The dealer shows the card face-up to all players;</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 5.3.2. The dealer announces that the specified card will be burned;</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 5.3.3. The dealer places the card into the used card box;</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 5.3.4. The dealer takes a new card from the shoe in the place of the burned card.</p>
    <p><strong>6.&nbsp;</strong><strong>Game organisation procedure:</strong></p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp;6.1. Draws of the game run 24/7 with maintenance breaks on demand, however, everything else including dealer and card changes are shown live for everyone to see.</p>
    <p><strong>7.&nbsp;</strong><strong>Equipment used in the game:</strong></p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 7.1. Six standard 52 (fifty-two) card decks. Each card has a unique bar code which is scanned by an integrated table scanner when the cards are dealt on the table.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 7.2. Integrated table scanner used to scan the bar codes on the cards.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 7.3. Security card used to cover the bottom card of the deck.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 7.4. Shoe with shuffled cards from which the dealer deals the cards.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 7.5. Table with two marked places for the Player and the Banker cards.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 7.6. Box for used cards where the dealer places the used cards after dealing.</p>
