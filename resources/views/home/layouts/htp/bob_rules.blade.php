    <p style="text-align: justify;"><strong>SHORT DESCRIPTION</strong></p>
    <p style="text-align: justify;">&ldquo;<strong>Bet-on-Baccarat</strong>&rdquo; is a real-time game with almost the same procedure and rules as Baccarat (or Punto Banco).</p>
    <p style="text-align: justify;">&nbsp;</p>
    <p style="text-align: justify;"><strong>GAME PROCESS</strong></p>
    <p style="text-align: justify;">The game is played between two sides &ndash; the Player and the Banker. The dealer deals two cards face-up to each side in every draw of the game. Cards are dealt one at a time and the Player always receives the first card. In special situations the third card is dealt to the Player and (or) the Banker. The goal of the game is to collect a total sum of points as close as possible or equal to 9 (nine) after no more cards can be dealt according to the rules. A Tie occurs if the Player and the Banker have the same number of points.&nbsp;</p>
    <p style="text-align: justify;">&nbsp;</p>
    <p style="text-align: justify;">The value of numerical cards from Deuce (2) to Nine (9) is equal to the number of points of the given card. The value of Face cards and Ten (10) is zero (0). Ace has a value of 1 (one) point. All card points are summed up to calculate the total sum of player&rsquo;s points. If the total sum is expressed in a double-digit figure, the score of the Player is the right digit of the total of the cards.</p>
    <p style="text-align: justify;">For example, the sum of Nine (9) and Four (4) is 13, so the score will be 3. The sum of Seven (7) and Eight (8) is 15, and the score will be 5.</p>
    <p style="text-align: justify;">&nbsp;</p>
    <p style="text-align: justify;">First round bets can be placed even before the opening of the first card. Punters can bet on one or more available outcomes. Second round bets can be placed when the Player receives the first card and odds are updated. Bets made in the previous betting round have no influence on later bets, therefore punters can place bets on the same or different outcomes more than once. Third round bets can be placed when the Banker receives the first card and odds update. After this betting round the Player and the Banker receive the second and, if necessary, the third card(cards). The results are calculated and the winner of the draw is displayed on screen.</p>
    <p style="text-align: justify;">&nbsp;</p>
    <p style="text-align: justify;">In case of a Tie, punters who placed bets on this outcome are paid their winnings. Bets placed on the Player and (or) the Banker are void and punters get their stakes back (odds are equalised to one (1.00)).</p>
