    <p><strong>GAME RULES</strong></p>
    <p style="text-align: justify;"><strong>1.&nbsp;</strong><strong>Terms:</strong></p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.1. Betting round &ndash; time allocated for accepting player bets.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.2. Odds &ndash; numerical expression determined on the basis of mathematical probability of an outcome, which is multiplied by the amount of the player&rsquo;s stake to calculate the winnings.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.3. Value &ndash; place of the card in the series of the same suit (e.g., Deuce, Jack, Ace).</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.4. Suit &ndash; one of the four types of cards (Hearts, Diamonds, Spades, Clubs).</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.5. Number type &ndash; odd or even.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.6. Deck &ndash; a set of 52 (fifty-two) cards consisting of thirteen cards from each of the four suits (Deuce, Three, Four, Five, Six, Seven, Eight, Nine, Ten, Jack, Queen, King, Ace).</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.7. Shoe &ndash; gaming device where six decks of cards are placed before starting the game.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.8. Draw &ndash; one dealing of cards which begins with the first betting round and ends when the dealer announces the outcome of the dealing, collects all used cards face-up on the table and puts them into the box for used cards.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.9. Betting outcome &ndash; possible interpretation of the outcome of one draw expressed in odds which may change in the course of the draw depending on the change of the mathematical probability of the outcome.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.10. Betting options &ndash; a list of all bets available to the player.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.11. Player &ndash; one of the parties of the game to whom the dealer deals the cards.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.12. Banker &ndash; one of the parties of the game to whom the dealer deals the cards.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.13. Tie &ndash; possible outcome of the game when Player and Banker have identical amount of points at the end of the draw.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.14. Natural hand &ndash; a situation when Player or Banker is dealt a total of 8 or 9 points in the first two cards. Natural hand of 9 is over Natural Hand of 8.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.15. Additional bets &ndash; betting outcomes that do not depend on the main betting outcomes (Player Pair, Banker Pair, Either Pair, Perfect Pair, Big, Small).</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.16. Player Pair &ndash; the first two cards of the Player of same value (e.g., a King of Clubs and a King of Spades).</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.17. Banker Pair &ndash; the first two cards of the Banker of same value (e.g., a Jack of Hearts and a Jack of Diamonds).</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.18. Any Pair &ndash; the first two cards of the Player or Banker that form a pair (e.g., Queen of Spades and Queen of Hearts).</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.19. Perfect Pair &ndash; the first two cards of the Player or Banker that form a suited pair (e.g., two Kings of Hearts).</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 1.20. Small &ndash; total of dealt Player and Banker cards is 4.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp;&nbsp;1.21. Big &ndash; total of dealt Player and Banker cards is 5 or 6.</p>
    <p><strong>2.&nbsp;</strong><strong>Rules:</strong></p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 2.1. The game is played between two sides &ndash; the Player and the Banker. The dealer of the game deals two cards to each side in every draw of the game. In special situations the dealer deals the third card to the Player and (or) the Banker. The rules of dealing the third card are described in paragraph 2.6.&nbsp;</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 2.2. Dealing:</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 2.2.1. Cards are dealt to sides by one card in turn until both sides have two cards face-up.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 2.2.2. The Player always receives the first card.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 2.2.3. When both sides have two cards face-up, the situation is assessed and in accordance with the fixed rules (see 2.6) it is determined whether the Player and (or) the Banker should receive the third card.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 2.3. The goal of the game is to collect a total of points as close as possible or equal to 9 (nine). The winner of the game is the side whose sum of points is closer to 9 (nine) after no more cards can be dealt according to the rules.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 2.3.1. Tie. A Tie occurs when the Player and the Banker have an equal sum of points. In the event of a Tie punters who placed bets on this outcome are paid their winnings. Bets placed on the Player and (or) the Banker are void and punters get their stakes back (odds are equalised to one (1.00)).</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 2.4. Card value in points:</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 2.4.1. Ace has the value of 1 (one) point.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 2.4.2. The sum of points of numerical cards from Deuce (2) to Nine (9) is equal to the number of points of the given card.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 2.4.3. The value of Face cards (Jack, Queen, King) and Ten (10) is zero (0).</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; 2.5. Calculation of the total of points:</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 2.5.1. All card points of each player are summed up to calculate the total sum of player&rsquo;s points.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 2.5.2. If the total sum is expressed in a double-digit figure, the score of the Player is the right digit of the total of cards. For example, the sum of Nine (9) and Four (4) is 13, so the score will be 3. The sum of Seven (7) and Eight (8) is 15, and the score will be 5.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 2.5.3. Additional bets:</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;2.5.3.1. In addition to the main bets like Player, Banker or Tie there are extra betting options such as bets on pairs and bets on the total of dealt cards. Odds for these outcomes change with each betting round.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;2.5.3.2. Bets on pairs (Player Pair, Banker Pair and Any Pair) apply only for the first two cards of the Player and Banker. The third card does not count and is not used to form pairs.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;2.5.3.3. Bets on the total of dealt cards can be placed as well. Bet on Small wins when the total of dealt cards is 4. Bet on Big wins when 5 or 6 cards are dealt.</p>
    <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;2.5.3.4. Additional bets are not linked with the main bets. It is possible to place the main bet on Player and additional bet on Banker Pair and vice versa. Additional bets can be placed without betting on the main bets.</p>
